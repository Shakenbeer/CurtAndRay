package com.shakenbeer.curtandray.game;


public class ImageInfo {
    final float centerX;
    final float centerY;
    final int width;
    final int height;
    final int radius;
    
    public ImageInfo(float centerX, float centerY, int width, int height, int radius) {
        super();
        this.centerX = centerX;
        this.centerY = centerY;
        this.width = width;
        this.height = height;
        this.radius = radius;
    }

    
}
